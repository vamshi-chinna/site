          <div class="row ">

      <p class="pb-3 pt-5 up-effect">
          <span style="font-size:20px;">Bunce Island is a small (502.9 meter by 106.7 meter) island
            located upriver from Freetown in the country of Sierra Leone. Despite its size,
            its location roughly where the broad Sierra Leone River grows shallow made it
            an ideal spot for slave vessels to anchor safely, sheltered from the Atlantic by
            the larger Tasso Island while maintaining access to the complex river system of
            the interior and the slave trade there.  This site saw 148000 enslaved Africans embarked,
              primarily at Bunce Island's slave fortress.
            <br><br>
           We are delighted to introduce our new project, which is focused to digitally rebuilding Bunce
            Island and the present-day ruins. Through a narrative structure, the users will be guided
            through an immersive digital experience to learn more about the complex history which
             took place here. Using Unreal's Metahumans and Unreal Engine 4.26, our team is developing
             a photorealistic environment harnessing photogrammetry and GIS technologies to bring
             innovative new teaching and research to the public.
        </p>



